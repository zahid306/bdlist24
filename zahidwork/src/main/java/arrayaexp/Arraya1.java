package arrayaexp;

public class Arraya1 {
///Arrays -Single Dimensions
	public static void main(String[] args) {

		int[] rahul = new int[8];

		rahul[0] = 22;
		rahul[1] = 76;
		rahul[2] = 40;
		rahul[3] = 23;
		rahul[4] = 75;
		rahul[5] = 45;
		rahul[6] = 74;
		rahul[7] = 41;

		for (int i = 0; i < rahul.length; i++) {
			System.out.println(rahul[i]);
		}

	}

}
